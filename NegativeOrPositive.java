public class NegativeOrPositive {

	public static void main(String[] args) {
		int i= -40;
		if(i>0) {
			System.out.println("Number is Positive");
		}
		else if(i==0) {
			System.out.println("Number is neither negative nor Positive");
		}
		else {
			System.out.println("Number is Negative");
		}
	}

}
